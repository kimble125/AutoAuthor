"""페이스북 포스트 (스토리텔링/바이럴) 기획 템플릿"""

class FacebookPostTemplate:
    platform = "facebook"
    display_name = "페이스북 바이럴 포스트"

    @staticmethod
    def generate_prompt(title: str, keywords: list[dict], content_type: str) -> str:
        kw_text = "\n".join(f"  - {k['keyword']}" for k in keywords[:6])

        return f"""당신은 페이스북에서 수만 명의 도달을 만들어내는 바이럴 마케터이자 탁월한 스토리텔러입니다.

'{title}' ({content_type})에 대한 페이스북 퍼블릭 포스트 기획안을 작성하세요.

[유입 최적화 키워드]
{kw_text}

[포스트 구조]
1. 훅 (Hook): 타임라인 스크롤을 멈추게 하는 강렬한 첫 2~3줄. (호기심 유발, 파격적 질문, 또는 깊은 공감)
2. '더보기(See More)' 유도: 첫 3줄 이후에 가장 중요한 내용이 이어지도록 구성.
3. 스토리텔링 본문: 
   - 딱딱한 정보 나열 금지.
   - 경험담, 깨달음, 또는 흥미로운 비하인드 스토리 형태로 전개.
   - 모바일 가독성을 위해 2~3줄마다 줄바꿈 필수.
4. 소셜 프루프/인사이트: "왜 사람들이 이 작품에 열광하는가?"에 대한 통찰 추가.
5. CTA (행동 유도): 지인 태그 유도 ("@이거 기억나?", "@이번 주말에 볼래?") 또는 의견 묻기.

[이미지/미디어 제안]
- 섬네일로 사용할 1장의 핵심 이미지 컨셉 또는 3~4장의 앨범 레이아웃 제안.
- 본문 중간중간 내용과 어울리는 이모지 적절히 혼합.

[해시태그]
- 페이스북 특성상 너무 많은 해시태그는 어색함. 가장 직관적인 해시태그 3~5개만 본문 하단에 배치.
"""
