"""유튜브 영상 스크립트 기획 템플릿"""


class YoutubeScriptTemplate:
    platform = "youtube"
    display_name = "유튜브 스크립트"

    @staticmethod
    def generate_prompt(title: str, keywords: list[dict], content_type: str) -> str:
        kw_text = "\n".join(f"  - {k['keyword']}" for k in keywords[:10])

        return f"""당신은 유튜브 영화/드라마 리뷰 크리에이터입니다.

'{title}' ({content_type}) 리뷰 영상 스크립트 기획안을 작성하세요.

[연관 키워드]
{kw_text}

[영상 구조] (목표: 8~12분)
1. 인트로 (0:00~0:30): 강력한 훅 + 채널 소개
2. 스포일러 없는 소감 (0:30~3:00): 첫인상, 분위기
3. 핵심 분석 1 (3:00~5:30): 스토리/연출 분석
4. 핵심 분석 2 (5:30~8:00): 캐릭터/메시지 해석
5. 비교/크로스 (8:00~9:30): 유사 작품과 비교
6. 별점 + 추천 (9:30~10:30): 총평
7. 아웃트로 (10:30~): 구독/좋아요 CTA

[유튜브 SEO]
- 제목: 50자 이내, 숫자/이모지 포함 권장
- 설명란 첫 3줄: 핵심 키워드 밀도 높게
- 태그: 20~30개
- 챕터 타임스탬프 필수
- 썸네일 컨셉: 1문장 설명

[출력]
- 영상 제목 2종
- 설명란 텍스트
- 태그 목록
- 챕터 타임스탬프
- 썸네일 컨셉
- 인트로 훅 대사 (정확한 스크립트)
"""
